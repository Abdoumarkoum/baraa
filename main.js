const increasebnt = document.getElementById("increasebnt");
const countlabel = document.getElementById("countlabel");

let count =0;

increasebnt.onclick = function(){
    count++;
    countlabel.textContent = count;
}



restbnt.onclick = function(){
    count = 0;
    countlabel.textContent = count;
}










